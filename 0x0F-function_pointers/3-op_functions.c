#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - adds
 * @a: first input
 * @b: second input
 * Return: integer
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts
 * @a: first input
 * @b: second input
 * Return: integer
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies
 * @a: first input
 * @b: second input
 * Return: integer
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - devides
 * @a: first input
 * @b: second input
 * Return: integer
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - give remaider
 * @a: first input
 * @b: second input
 * Return: integer
*/
int op_mod(int a, int b)
{
	return (a % b);
}

